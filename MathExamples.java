public class MathExamples {
    public static void main(String[] args) {
        System.out.println(Math.ceil(2.1));
        System.out.println(Math.floor(2.9));
        System.out.println(Math.rint(3.4));
        System.out.println(Math.round(2.4f));
        System.out.println(Math.max(11,10.0));
        System.out.println(Math.min(11,10.0));
        System.out.println(11 + 10.0);
        System.out.println(Math.round(11 + 10.0));
        System.out.println(Math.pow(11,2));
        System.out.println(Math.exp(2));
        System.out.println(Math.log(2));
        System.out.println(Math.sqrt(121));
        
    }
    
}
